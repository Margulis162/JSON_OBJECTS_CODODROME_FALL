// vars and classes 

class Cultist {
    first_name;
    last_name;
    age;
    gender;
    phone;
    email;
    additional_data;

    constructor(first_name, last_name, age, gender, phone, email, additional_data,){
        this.first_name = first_name;
        this.last_name = last_name;
        this.age = age;
        this.gender = gender;
        this.phone = phone;
        this.email = email;
        this.additional_data = additional_data;
    }
}

